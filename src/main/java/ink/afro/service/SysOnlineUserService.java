package ink.afro.service;

import ink.afro.entity.SysOnlineUser;
import ink.afro.entity.other.LoginUser;

/**
 * 在线用户 服务接口
 *
 * @author goodboy95-code
 */
public interface SysOnlineUserService {
    /**
     * 通过登录地址查询信息
     *
     * @param ipaddr 登录地址
     * @param user   用户信息
     * @return 在线用户信息
     */
    SysOnlineUser getOnlineUserByIpaddr(String ipaddr, LoginUser user);

    /**
     * 通过用户名称查询信息
     *
     * @param userName 用户名称
     * @param user     用户信息
     * @return 在线用户信息
     */
    SysOnlineUser getOnlineUserByUserName(String userName, LoginUser user);

    /**
     * 通过登录地址/用户名称查询信息
     *
     * @param ipaddr   登录地址
     * @param userName 用户名称
     * @param user     用户信息
     * @return 在线用户信息
     */
    SysOnlineUser getOnlineUserByInfo(String ipaddr, String userName, LoginUser user);

    /**
     * 设置在线用户信息
     *
     * @param user 用户信息
     * @return 在线用户
     */
    SysOnlineUser loginUserToOnlineUser(LoginUser user);
}
